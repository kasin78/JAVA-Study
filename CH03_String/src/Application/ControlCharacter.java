package Application;

public class ControlCharacter {

	public static void main(String[] args) {
		// 제어 문자  
			// \t : 탭
			// \n : 줄 바꿈
		
		// System.out.println(); : 맨끝에 줄 바꿈 기본 포함되어 출력
		System.out.println("======================");
		System.out.println("\t탭\t탭만큼 뛰웁니다.");
		System.out.println("======================");
		System.out.println("자바를\n배우고\n있습니다");
		
		
		// System.out.print(); : 공백 없이 문자열 출력
		System.out.print("이것은");
		System.out.print("줄을");
		System.out.print("안 내립니다.");
		
	}

}
