package parameter;

public class Calculator {
	int square(int x) {
		return x * x; // 입력된 정수 x의 값을 리턴식으로 계산해서 리턴
	}
	// 사칙연산
	int plus(int x, int y) {
		return x + y;
	}
	int minus(int x, int y) {
		return x - y;
	}
	int divide(int x, int y) {
		return x / y;		
	}
	int multi(int x, int y) {
		return x * y;		
	}
}
